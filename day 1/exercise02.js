// find perimeter of rectangle

// 2 variable : panjang dan lebar
// cara mencari perimeter : 2 dikali (panjang tambah lebar)
// menampilkan hasil perimeter di dalam terminal

let length = 5; //panjang
let width = 3; //lebar

let perimeter = 2 * (length + width);

console.log(`perimeter of rectangle : ${perimeter}`);
